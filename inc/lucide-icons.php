if (!function_exists('bsi_lucide_icon')) {
	/**
	 * Возвращает готовый inline-SVG для иконки Lucide.
	 *
	 * @param string $name        Имя иконки (kebab-case).
	 * @param array  $attrs       Доп. атрибуты <svg>: width, height, class, stroke и т.п.
	 * @return string             Готовый <svg ...>...</svg> либо пустая строка.
	 */
	function bsi_lucide_icon(string $name, array $attrs = []): string
	{
		$name = strtolower(trim($name));
		if ($name === '') {
			return '';
		}

		$library = bsi_lucide_icon_library();
		if (!isset($library[$name])) {
			return '';
		}

		$defaults = [
			'width' => '24',
			'height' => '24',
			'viewBox' => '0 0 24 24',
			'fill' => 'none',
			'stroke' => '#EF0101',
			'stroke-width' => '2',
			'stroke-linecap' => 'round',
			'stroke-linejoin' => 'round',
			'class' => 'lucide lucide-' . $name,
			'aria-hidden' => 'true',
			'focusable' => 'false',
		];

		$merged = array_merge($defaults, $attrs);

		$attr_str = '';
		foreach ($merged as $key => $value) {
			if ($value === null || $value === '') {
				continue;
			}
			$attr_str .= ' ' . esc_attr($key) . '="' . esc_attr((string) $value) . '"';
		}

		return '<svg xmlns="http://www.w3.org/2000/svg"' . $attr_str . '>' . $library[$name] . '</svg>';
	}
}
